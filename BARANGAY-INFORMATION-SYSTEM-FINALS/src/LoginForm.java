    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String user = txtUsername.getText();
        String pass = new String(txtPassword.getPassword());

        // Developer login (optional)
        if (user.equals("mike") && pass.equals("1234")) {
            progressbar();
            JOptionPane.showMessageDialog(this, "Developer Log in Successfully", "Login", JOptionPane.PLAIN_MESSAGE);
            new DeveloperPage().setVisible(true);
            this.dispose();
            return;
        }

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = aSQL.getConnection();

            String sql = "SELECT * FROM barangay_finals.information WHERE username=? AND password=?";
            PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, user);
            pst.setString(2, pass);

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                int id = rs.getInt("citizen_id"); // Get the ID of the logged-in user

                // Store user data in session
                Citizen_Login.setUser(id, user, pass);
                
                progressbar();
                JOptionPane.showMessageDialog(this, "Login Successful!");
                new Homepage().setVisible(true); // Open homepage
                this.dispose(); // Close login window
            } else {
                JOptionPane.showMessageDialog(this, "Invalid Username or Password", "Error", JOptionPane.ERROR_MESSAGE);
                txtPassword.setText("");
                txtUsername.setText("");
            }

            rs.close();
            pst.close();
            con.close();

        } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
                txtPassword.setText("");
                txtUsername.setText("");
        }

        
     
    }//GEN-LAST:event_jButton2ActionPerformed

