    private void btnSHOWActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSHOWActionPerformed
        int selectedRow = jTable1.getSelectedRow();
            if (selectedRow >= 0) {
                // Get the citizen_id from the selected row
                String citizenId = jTable1.getValueAt(selectedRow, 0).toString();

            try {
                
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection conn = aSQL.getConnection();

                String sql = "SELECT * FROM barangay_finals.information WHERE citizen_id = ?";
                PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.setString(1, citizenId);

                ResultSet rs = stmt.executeQuery();
                
                while (rs.next()) {
                
                    // DOC_DETAIL1
                    byte[] imgDoc1Bytes = rs.getBytes("DOC_DETAIL1");
                    if (imgDoc1Bytes != null) {
                        ImageIcon icon = new ImageIcon(imgDoc1Bytes);
                        Image img = icon.getImage().getScaledInstance(imgDOC1.getWidth(), imgDOC1.getHeight(), Image.SCALE_SMOOTH);
                        imgDOC1.setIcon(new ImageIcon(img));
                    }

                    // DOC_DETAIL2
                    byte[] imgDoc2Bytes = rs.getBytes("DOC_DETAIL2");
                    if (imgDoc2Bytes != null) {
                        ImageIcon icon = new ImageIcon(imgDoc2Bytes);
                        Image img = icon.getImage().getScaledInstance(imgDOC2.getWidth(), imgDOC2.getHeight(), Image.SCALE_SMOOTH);
                        imgDOC2.setIcon(new ImageIcon(img));
                    }

                    // VALID_ID1
                    byte[] imgValid1Bytes = rs.getBytes("VALID_ID1");
                    if (imgValid1Bytes != null) {
                        ImageIcon icon = new ImageIcon(imgValid1Bytes);
                        Image img = icon.getImage().getScaledInstance(imgVALID1.getWidth(), imgVALID1.getHeight(), Image.SCALE_SMOOTH);
                        imgVALID1.setIcon(new ImageIcon(img));
                    }

                    // VALID_ID2
                    byte[] imgValid2Bytes = rs.getBytes("VALID_ID2");
                    if (imgValid2Bytes != null) {
                        ImageIcon icon = new ImageIcon(imgValid2Bytes);
                        Image img = icon.getImage().getScaledInstance(imgVALID2.getWidth(), imgVALID2.getHeight(), Image.SCALE_SMOOTH);
                        imgVALID2.setIcon(new ImageIcon(img));
                    }

                    // IMAGE_ID (for ID Picture)
                    byte[] imgIDBytes = rs.getBytes("Image_ID");
                    if (imgIDBytes != null) {
                        ImageIcon icon = new ImageIcon(imgIDBytes);
                        Image img = icon.getImage().getScaledInstance(imgID.getWidth(), imgID.getHeight(), Image.SCALE_SMOOTH);
                        imgID.setIcon(new ImageIcon(img));
                    }
                
                }
                
                
            } catch (Exception e){}
            
            }// TODO add your handling code here:
    }//GEN-LAST:event_btnSHOWActionPerformed


